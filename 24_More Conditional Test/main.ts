// Equality with strings
console.log("Testing equality with strings:");
let fruit : string = "mango";
console.log(fruit == "mango"); // True
console.log(fruit ==  "Mango"); // False

// Using the UpperCase case function
console.log("Testing with Upper case:");
console.log("Apple".toUpperCase() == "Apple"); // False

// Numerical tests
console.log("Numerical tests:");
console.log(5 > 1); // True
console.log(1 <= 1); // False

// Tests using "and" and "or" operators
console.log("Tests with 'and' and 'or':");
console.log(true && false); // False
console.log(true || false); // True

// Test whether an item is in a array
let fruits = ["apple", "pineapple", "banana"];
console.log("Is 'apple' in fruits?");
console.log(fruits.includes("apple")); // True

// Test whether an item is not in a array
console.log("Is 'mango' in fruits?");
console.log(fruits.includes("mango")); // False


















